package chapter02;

public class VariableIntegerTest {
	
	public static void main(String[] args) {
		
		
		// 정수 : byte      short     int(기본타입)    long 
		//  	 1byte     2byte     4byte         8byte
		
		int num = 100;
		
		byte byteNum1 = 10;
		byte byteNum2 = 127;
		byte byteNum3 = (byte)200;
		
		System.out.println(byteNum1);
		System.out.println(byteNum2);
		System.out.println(byteNum3);
		
		long longNum1 = 2300000000L; // 마지막에 L을 붙여 long 으로 변환되어 오류가 나지 않도록 할 수 있다.
		
		int num1 = 100000;
		int num2 = 100000;
		
		// 곱하기 연산 
		long result = (long)num1 * num2; // long * long = long
					// ㄴ long 타입으로 강제적으로 변환 
		System.out.println(result);
		
		
		
		
		
		
		
	}
	

	
	
}
