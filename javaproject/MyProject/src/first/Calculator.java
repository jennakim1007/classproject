package first;

public class Calculator {
	
	public static void main(String[] args) {
		
		int sum = add(80, 20);
		System.out.println(sum);
	
		int m = m(100,40);
		System.out.println(m);
		
	}
	
	public static int add(int n1, int n2) {
		int sum = n1 + n2;
		return sum;
	}
	
	public static int m(int n3, int n4) {
		int m = n3 - n4;
		return m;
		
	}
	
	
}
	
	

